    private void loadOrdersToTable() {
        
    // Dapatkan model tabel
    DefaultTableModel model = (DefaultTableModel) rincian.getModel();

    // Bersihkan tabel sebelum mengisi data baru
    model.setRowCount(0);

    // Ambil data dari database
    OrderRepository orderRepo = new OrderRepository();
    List<Object[]> orders = orderRepo.getOrders();

    // Isi tabel dengan data dari database
    for (Object[] order : orders) {
        model.addRow(order);
    }

    System.out.println("Data pesanan berhasil dimuat ke tabel.");
    }
