    private void EditBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditBTActionPerformed
        // TODO add your handling code here:
        int selectedRow = rincian.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris yang ingin diedit.");
            return;
        }
        String newMenu = JOptionPane.showInputDialog(this, "Masukkan nama menu baru:");
        String newHarga = JOptionPane.showInputDialog(this, "Masukkan jumlah baru:");
        String newJumlah = JOptionPane.showInputDialog(this, "Masukkan harga baru:");
        rincian.setValueAt(newMenu, selectedRow, 0);
        rincian.setValueAt(newHarga, selectedRow, 1);
        rincian.setValueAt(newJumlah, selectedRow, 2);
    }//GEN-LAST:event_EditBTActionPerformed

