    private void BayarBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BayarBTActionPerformed
        // TODO add your handling code here:
        try {
            if (txtTotalHarga.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Total harga kosong. Silakan tambahkan item terlebih dahulu.");
            return;
        }
            double totalHarga = Double.parseDouble(txtTotalHarga.getText());
            double bayar = Double.parseDouble(txtBayar.getText());

            if (bayar >= totalHarga) {
                double kembalian = bayar - totalHarga;
                JOptionPane.showMessageDialog(this, "Pembayaran berhasil! Kembalian: " + kembalian);
            } else {
                JOptionPane.showMessageDialog(this, "Jumlah bayar kurang!");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Masukkan angka yang valid di kolom Bayar!");
        }
    }//GEN-LAST:event_BayarBTActionPerformed

