    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        // TODO add your handling code here:
             


        // Membuka GUI Pembayaran
    Pembayaran pembayaran = new Pembayaran();
    pembayaran.setVisible(true);

    // Menutup GUI Pemesanan saat ini
    this.dispose();

    // Menambahkan contoh logika opsional jika Anda ingin memproses data sebelum berpindah
    // Simpan rincian pesanan atau lakukan validasi jika diperlukan
    try {
        OrderRepository orderRepo = new OrderRepository(); // Asumsikan ada kelas OrderRepository
        for (int i = 0; i < rincian.getRowCount(); i++) {
            String menuName = (String) rincian.getValueAt(i, 0); // Kolom nama menu
            int quantity = (int) rincian.getValueAt(i, 1); // Kolom jumlah pesanan
            double price = quantity * 10_000; // Contoh logika harga
            String paymentMethod = "Belum dipilih"; // Bisa diganti setelah pembayaran

            orderRepo.saveOrder(menuName, quantity, price, paymentMethod);
        }
        System.out.println("Pesanan berhasil disimpan sebelum berpindah ke halaman pembayaran.");
    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menyimpan pesanan: " + ex.getMessage());
    }

    }//GEN-LAST:event_btnBayarActionPerformed

